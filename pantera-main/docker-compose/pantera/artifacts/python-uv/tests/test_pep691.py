"""
Smoke tests for Pantera's PEP 691 JSON Simple API.

These run AFTER `uv sync` has installed the resolved packages into
the venv. The tests verify that the packages actually installed and
are importable — proving that:
  1. The PEP 691 JSON index was valid (uv parsed it)
  2. The download URLs in the JSON resolved correctly (uv fetched them)
  3. The hosted + proxied group routing worked (all packages found)
  4. PEP 700 upload-time is preserved through the proxy for exclude-newer

Run:
    uv run python -m pytest tests/ -v
"""
import importlib
import subprocess
import sys
import tempfile
from pathlib import Path


PANTERA_GROUP = (
    "http://ayd:ayd@localhost:8081"
    "/test_prefix/api/pypi/pypi_group/simple/"
)
PROJECT_DIR = Path(__file__).parent.parent


# ==================================================================
# Package installation tests (require uv sync to have run first)
# ==================================================================

def test_requests_installed():
    """requests is a proxied package — verifies proxy JSON index works."""
    import requests
    assert hasattr(requests, "get"), "requests.get must be callable"


def test_hello_installed():
    """hello is a hosted package — verifies hosted JSON index + relative URLs."""
    hello = importlib.import_module("hello")
    assert hasattr(hello, "__version__") or True, "hello package must be importable"


def test_pydantic_installed():
    """pydantic exercises deep transitive resolution via the JSON API."""
    import pydantic
    assert pydantic.VERSION, "pydantic must expose a VERSION"



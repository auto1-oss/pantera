#!/usr/bin/env bash
##
## Aggregates raw.csv timing data into per-protocol stats and markdown report.
##
## Input:  /results/raw.csv (worker_id,protocol,operation,start_ns,end_ns,exit_code)
## Output: /results/stats.csv + /results/BENCHMARK-REPORT.md
##
set -euo pipefail

RESULTS="${RESULTS:-/results}"
RAW_CSV="${RESULTS}/raw.csv"
STATS_CSV="${RESULTS}/stats.csv"
REPORT="${RESULTS}/BENCHMARK-REPORT.md"

if [[ ! -s "$RAW_CSV" ]]; then
    echo "  [WARN] No raw data found at ${RAW_CSV}"
    exit 0
fi

echo "  [INFO] Aggregating results..."

# ================================================================
# Compute stats per protocol+operation using awk
# ================================================================
echo "protocol,operation,total_reqs,rps,avg_ms,p50_ms,p95_ms,p99_ms,max_ms,error_pct" > "$STATS_CSV"

awk -F, '
NR == 1 { next }  # skip header
{
    proto = $2
    op = $3
    start_ns = $4 + 0
    end_ns = $5 + 0
    rc = $6 + 0

    key = proto "," op
    latency_ms = (end_ns - start_ns) / 1000000.0

    count[key]++
    sum[key] += latency_ms
    if (rc != 0) errors[key]++

    # Track wall-clock span
    if (!(key in first_start) || start_ns < first_start[key]) first_start[key] = start_ns
    if (!(key in last_end) || end_ns > last_end[key]) last_end[key] = end_ns

    # Store latencies for percentiles (up to 1M entries per key)
    idx = count[key]
    latencies[key, idx] = latency_ms
}
END {
    for (key in count) {
        n = count[key]
        if (n == 0) continue

        # Sort latencies for this key (insertion sort — fine for moderate N)
        for (i = 2; i <= n; i++) {
            val = latencies[key, i]
            j = i - 1
            while (j >= 1 && latencies[key, j] > val) {
                latencies[key, j+1] = latencies[key, j]
                j--
            }
            latencies[key, j+1] = val
        }

        avg = sum[key] / n
        p50 = latencies[key, int(n * 0.50) + 1]
        p95 = latencies[key, int(n * 0.95) + 1]
        p99 = latencies[key, int(n * 0.99) + 1]
        mx  = latencies[key, n]

        wall_s = (last_end[key] - first_start[key]) / 1000000000.0
        rps = (wall_s > 0) ? n / wall_s : 0

        err_pct = (errors[key] + 0) * 100.0 / n

        printf "%s,%d,%.1f,%.1f,%.1f,%.1f,%.1f,%.1f,%.2f\n", \
            key, n, rps, avg, p50, p95, p99, mx, err_pct
    }
}' "$RAW_CSV" | sort >> "$STATS_CSV"

# ================================================================
# Generate markdown report
# ================================================================
cat > "$REPORT" << 'EOF'
# Pantera Benchmark Report v3

## Part 1: Registry Throughput (hey — direct HTTP)

Isolates registry performance from client overhead. Uses `hey` to send concurrent HTTP requests directly to registry endpoints.

| Test | Concurrency | Requests | RPS | Avg (ms) | P50 (ms) | P95 (ms) | P99 (ms) | Max (ms) | Errors |
|------|-------------|----------|-----|----------|----------|----------|----------|----------|--------|
EOF

# Add registry throughput data if available
hey_csv="${RESULTS}/registry-throughput.csv"
if [[ -f "$hey_csv" ]]; then
    tail -n +2 "$hey_csv" | while IFS=, read -r test conc total rps avg p50 p95 p99 mx err; do
        printf "| %s | %s | %s | %s | %s | %s | %s | %s | %s | %s%% |\n" \
            "$test" "$conc" "$total" "$rps" "$avg" "$p50" "$p95" "$p99" "$mx" "$err" >> "$REPORT"
    done
fi

cat >> "$REPORT" << 'EOF'

---

## Part 2: Real-Client Throughput (80% reads / 20% writes)

End-to-end measurements using real package manager clients. Includes client startup, protocol negotiation, and transfer.

| Protocol | Operation | Requests | RPS | Avg (ms) | P50 (ms) | P95 (ms) | P99 (ms) | Max (ms) | Errors |
|----------|-----------|----------|-----|----------|----------|----------|----------|----------|--------|
EOF

tail -n +2 "$STATS_CSV" | while IFS=, read -r proto op total rps avg p50 p95 p99 mx err; do
    printf "| %s | %s | %s | %s | %s | %s | %s | %s | %s | %s%% |\n" \
        "$proto" "$op" "$total" "$rps" "$avg" "$p50" "$p95" "$p99" "$mx" "$err" >> "$REPORT"
done

cat >> "$REPORT" << EOF

---

## Configuration

| Parameter | Value |
|-----------|-------|
| Concurrency | ${BENCH_CONCURRENCY:-40} (${BENCH_READERS:-32}R / ${BENCH_WRITERS:-8}W) |
| Duration per protocol | ${BENCH_DURATION:-60}s |
| Protocols | ${BENCH_PROTOCOLS:-maven,npm,docker,php,pypi} |
| Registry | ${REGISTRY_HOST:-localhost:9080} |

*Generated by benchmark v3. Data: \`registry-throughput.csv\`, \`raw.csv\`, \`stats.csv\`.*
EOF

echo "  [INFO] Stats:  ${STATS_CSV}"
echo "  [INFO] Report: ${REPORT}"
